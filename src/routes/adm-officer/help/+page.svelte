  <div class="bg-white shadow rounded-lg p-6 mb-8">
    <h2 class="text-2xl font-semibold mb-4 text-green-600">Verification & Approval</h2>
    <p class="mb-4 text-gray-600">
      Your core responsibility is the thorough verification of applications for final admission.
    </p>

    <h3 class="text-xl font-medium mb-3">Verifying Applications</h3>
    <ol class="list-decimal list-inside mb-4 text-gray-600 space-y-2">
      <li>Navigate to the <strong>Applications</strong> list.</li>
      <li>Filter by status <strong>Verified</strong> (or similar pre-verified stage).</li>
      <li>Review the student's complete profile, including academic records and documents.</li>
      <li>Cross-check marks and eligibility criteria.</li>
      <li>Click <strong>Approve</strong> for admission or <strong>Needs Correction/Reject</strong>.</li>
    </ol>
  </div>

  <div class="bg-white shadow rounded-lg p-6 mb-8">
    <h2 class="text-2xl font-semibold mb-4 text-blue-600">Merit Lists & Reports</h2>
    <h3 class="text-xl font-medium mb-3">Generating Merit Lists</h3>
    <ol class="list-decimal list-inside mb-4 text-gray-600 space-y-2">
      <li>Go to the <strong>Merit List</strong> section.</li>
      <li>Select the relevant course and academic session.</li>
      <li>Click <strong>Generate List</strong>.</li>
      <li>Review the ranked list of candidates based on merit scores.</li>
      <li>Publish the list when finalized.</li>
    </ol>

    <h3 class="text-xl font-medium mb-3">Admission Reports</h3>
    <p class="mb-4 text-gray-600">
      Access comprehensive reports on admitted students, filled seats, and category-wise breakdown in the <strong>Reports</strong> tab.
    </p>
  </div>
